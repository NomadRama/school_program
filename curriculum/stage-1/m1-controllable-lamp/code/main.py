# Модуль 1 — Управляемая лампа
# Референсный код MicroPython для Raspberry Pi Pico.
# Мигает LED через реле на пине GP15.
#
# Схема подключения:
#   Pico GP15   -> IN реле  (сигнал управления)
#   Pico 3V3    -> VCC реле (питание катушки)
#   Pico GND    -> GND реле
#   ATX +3.3V   -> COM реле (силовой вход)
#   Реле NO     -> +LED     (плюс светодиода)
#   LED -       -> ATX GND  (возврат тока)

from machine import Pin
from time import sleep

# Настраиваем пин GP15 как выход — будем подавать 0 или 3.3V на IN реле
relay = Pin(15, Pin.OUT)

# Бесконечный цикл: включаем реле на 1 секунду, потом выключаем на 1 секунду
while True:
    relay.value(1)   # подать 3.3V на IN — реле замыкает силовую цепь → LED горит
    sleep(1)         # ждать 1 секунду

    relay.value(0)   # подать 0V на IN — реле размыкает силовую цепь → LED гаснет
    sleep(1)         # ждать 1 секунду
